#[cfg(test)]
mod tests {
    use std::fs;
    use std::path::PathBuf;

    use super::*;

    #[test]
    fn generated_bindings_are_current() {
        let path = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
            .join("../../apps/desktop-ui/src/generated/contracts.ts");
        let checked_in = fs::read_to_string(&path)
            .unwrap_or_else(|error| panic!("failed to read {}: {error}", path.display()));

        assert_eq!(
            checked_in,
            typescript_bindings(),
            "{} is stale; regenerate it from wardrobe-core",
            path.display()
        );
    }
}
